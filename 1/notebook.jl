First we need to calculate the sliding sums:
"

# ╔═╡ 1c07bf8a-9539-48c4-a824-2d7d5de08a6a
function sliding_sums(pattern, width=3)
	if width <1
		error("How would that work?")
	end
	sums = []
	for i in (1:(length(pattern) - (width-1)))
		sum = 0
		for k in (0:width-1)
			sum += pattern[i+k]
		end
		append!(sums, sum)
	end
	sums
end

# ╔═╡ a7ddeff9-310d-4ef2-bf9b-484a32d56297
sums = sliding_sums(example, 3)

