# ╔═╡ 04ce783e-0918-44cd-9bbd-b0bb999dc8f9
function solution_1(pattern)
	begin
		previous = Nothing
		counter = 0
		for number in pattern
			if previous != Nothing
				if previous < number
					counter += 1
				end
			end
			previous = number
		end
	end
	counter
end

